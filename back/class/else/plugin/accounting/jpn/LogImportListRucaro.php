class Code_Else_Plugin_Accounting_Jpn_LogImportListRucaro extends Code_Else_Plugin_Accounting_Jpn_LogImportList
{
	protected $_childSelf = array(

	);


	/**
	 *
	 */
	public function run()
	{
		global $varsRequest;

		if ($varsRequest['query']['func']) {
			$method = '_ini' . $varsRequest['query']['func'];
			if (method_exists($this, $method)) {
				$this->$method();

			} else {
				if (FLAG_TEST) {
					var_dump(__CLASS__ . '/' .__FUNCTION__. '/' .__LINE__);
				}
				exit;
			}
		}
		exit;
	}

	/**
	 *
	 */
	protected function _iniDetailAdd()
	{
		$this->_setDetailAdd();
	}

}
